# To check whether a character is vowel or not

str1 = input("Enter a character: ")

ch = str1[0]

if ch == "a" or ch == "A":
    print("Vowel")

elif ch == "e" or ch == "E":
    print("Vowel")

elif ch == "i" or ch == "I":
    print("Vowel")

elif ch == "o" or ch == "O":
    print("Vowel")

elif ch == "u" or ch == "U":
    print("Vowel")

else:
    print("Consonant")
